    private void BorrarCampos(){
        TextFieldCodigoLibro.setText(null);
        TextFieldTitulo.setText(null);
        TextFieldAutor.setText(null);
        TextFieldPrecio.setText(null);

    }
    
    private void desbloquearBotones() {
        TextFieldCodigoLibro.setEditable(false);
        ButtonNuevo.setEnabled(false);
        ButtonBorrar.setEnabled(true);
        ButtonModificar.setEnabled(true);
        ButtonCancelar.setEnabled(true);
    }
    
    private void BloquearBotones() {
        TextFieldCodigoLibro.setEditable(true);
        ButtonNuevo.setEnabled(true);
        ButtonBorrar.setEnabled(false);
        ButtonModificar.setEnabled(false);
        ButtonCancelar.setEnabled(false);
    }
          
