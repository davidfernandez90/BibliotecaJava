    private void ButtonNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonNuevoActionPerformed
        
        Libro libro = new Libro();
        
        if(dao==null){
            JOptionPane.showMessageDialog(this, "Es necesario "
            + "abrir una Base de datos.");
            return;
        }
        try{
            libro.setCodigoLibro(TextFieldCodigoLibro.getText());
            libro.setTitulo(TextFieldTitulo.getText());
            libro.setAutor(TextFieldAutor.getText());
            libro.setPrecio(Integer.valueOf(TextFieldPrecio.getText()));
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Ingrese un precio valido");
            return;
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return;
        }
                      
        try {
            dao.insertarLibro(libro); //no me permite genrar la excepciÃÂ³n 
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return; 
        }
        JOptionPane.showMessageDialog(this, "El libro se agrego correctamente.");
        BorrarCampos();     
             
    }//GEN-LAST:event_ButtonNuevoActionPerformed

